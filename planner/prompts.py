def detect_doc_category(topic: str) -> str:
    """根据规划话题判断应该展示哪类文档菜单。"""
    lower = topic.lower()
    if any(s in lower for s in _TRAVEL_SIGNALS):
        return "travel"
    if any(s in lower for s in _MARKETING_DOC_SIGNALS):
        return "marketing"
    if any(s in lower for s in _PROJECT_SIGNALS):
        return "project"
    return "general"


def build_doc_menu(category: str) -> dict[str, object]:
    """根据话题类别生成 DOC_MENU 映射（用户输入 → doc_type）。"""
    extras = DOC_TOPIC_EXTRAS.get(category, [])
    all_types = DOC_BASE_TYPES + extras
    menu: dict[str, object] = {}
    for i, dt in enumerate(all_types, 1):
        menu[str(i)] = dt
    menu["全部"] = list(all_types)
    menu["all"] = list(all_types)

    _KEYWORD_MAP = {
        "checklist": ["checklist", "清单", "todo", "待办"],
        "proposal": ["提案", "proposal", "方案提案"],
        "decision": ["决策", "一页纸", "decision"],
        "brief": ["brief", "执行"],
        "timeline": ["排期", "时间线", "里程碑", "timeline"],
        "summary": ["摘要", "总结", "summary", "存档"],
        "itinerary": ["行程", "itinerary", "行程表"],
        "budget": ["预算", "budget", "花费"],
        "spec": ["spec", "规格", "scope"],
        "features": ["功能", "优先级", "feature", "p0"],
        "calendar": ["日历", "calendar", "内容日历"],
    }
    for dt in all_types:
        for kw in _KEYWORD_MAP.get(dt, []):
            if kw not in menu:
                menu[kw] = dt
    return menu


