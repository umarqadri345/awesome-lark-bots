PITCH_INSTRUCTION = """现在请你为以下营销课题出一份完整提案。

输出结构（每节用 ## 标题）：

## 洞察
从你的 Agency 视角，你看到了什么别人没注意到的机会？
为什么**现在**是做这件事的最佳时机？
2-3 句话，**加粗**最锐利的判断。

## 核心创意
一句话概念（≤20 字，能直接当 slogan 用）。
然后 3-5 句话讲创意机制：它怎么运作、用户为什么会买账、和竞品的区别在哪。

## 执行方案
3-5 步落地路径，每步：
- **动作**（一句话）+ **产出**（做完手上多了什么）
- 时间建议
- 需要的资源

## 传播设计
- 用户会怎么自发传播？（具体到发什么平台、配什么文案模板）
- 传播的"钩子"是什么——看到就想转发的那个点
- 预期传播路径：种子用户 → 第一波扩散 → 破圈

## 预期效果
- 短期（1 个月内）：具体数字 + 假设前提
- 中期（3 个月）：具体数字 + 假设前提
- 用 **加粗** 标记关键数字

## 预算与 ROI
- 分项预算（人力/媒介/制作/其他）
- 预期 ROI 计算逻辑
- 和纯投放相比，这个方案的成本优势在哪

## 为什么选我们
一段话：如果客户只能选一家 Agency，为什么应该选你？
不是自夸，是说你的方案解决了什么其他方案解决不了的问题。

500-1000 字。中文输出。"""


