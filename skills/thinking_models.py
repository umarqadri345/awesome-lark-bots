"""
通用思维模型技能 — 适用于生活、职业、个人决策等非商业场景。

与 decision_frameworks 互补：
  decision_frameworks = 商业/项目决策工具（MECE、约束理论、pre-mortem）
  thinking_models     = 日常判断力工具（后悔最小化、沉没成本、精力审计）
"""

from skills import Skill, register

_MODELS = """
## 通用思维工具箱

这些是帮你在任何话题上想得更清楚的工具。用它们思考，但像正常人说话。

---

### 1. 后悔最小化（Regret Minimization）

做选择时问自己：80 岁回头看，会后悔没做哪个？

适用：职业转型、要不要尝试新事物、大的生活决定。
核心：短期不适 vs 长期遗憾——大多数人高估前者，低估后者。

---

### 2. 沉没成本觉察（Sunk Cost Awareness）

已经花掉的时间/钱/精力不应该影响下一步决定。
只问"从现在起，哪个选择更好"，不问"之前投入了多少"。

适用：要不要放弃一个进行中的事、继续还是止损。
反面教材："都已经做了这么多了，不做完可惜" — 这句话 90% 的时候是陷阱。

---

### 3. 二阶思考（Second-Order Thinking）

不止想"这个决定会怎样"，还要想"然后呢？"

操作：
- 一阶：做了 A 会怎样？
- 二阶：别人会怎么反应？会带来什么新问题？
- 三阶：新问题会把我推向哪？

适用：人际关系决策、职业选择、任何有连锁反应的事。

---

### 4. 精力审计（Energy Audit）

不是所有产出相等。区分"给你充电的事" vs "耗电的事"。

操作：
- 列出这周做的事，标注每件事结束后的能量状态（↑ 充电 / → 中性 / ↓ 耗电）
- 耗电的事：能砍掉？能委托？能换方式做？
- 充电的事：能多做？能把它变成工作的一部分？

适用：觉得累但说不清为什么、工作生活平衡、选 side project 方向。

---

### 5. 反转思维（Inversion）

不问"怎么成功"，先问"怎么一定会失败"，然后避开。

操作：
- "如果我想让这件事一定失败，我会做什么？"
- 列出 3 个必败操作
- 检查自己是不是正在做其中某个

适用：计划执行、习惯养成、关系维护。

---

### 6. 选项价值（Optionality）

优先选择保持更多未来选项的路径。

原则：
- 不确定性高时，选"保留选择权"的方案，不选"锁死路径"的
- 可以低成本试的事，先试再说
- 需要大量不可逆投入的事，等信息更充分再决定

适用：职业规划、要不要 all-in、投资（时间/金钱）决策。

---

### 7. 10/10/10 法则

问自己：这个决定，10 分钟后我会怎么想？10 个月后呢？10 年后呢？

适用：情绪化决定、冲动购买、人际冲突中要不要发火。
核心：拉开时间距离看问题。

---

### 使用原则

- 这些工具是帮你想清楚的，不是用来写报告的。
- 对话中自然运用，不要列"思维模型清单"。
- 生活问题优先用：后悔最小化 + 精力审计 + 10/10/10。
- 职业问题优先用：选项价值 + 二阶思考 + 沉没成本。
"""


class ThinkingModelsSkill(Skill):
    name = "thinking_models"
    description = "通用思维模型 — 后悔最小化、精力审计、二阶思考等日常判断力工具"
    trigger_keywords = [
        "纠结", "犹豫", "要不要", "该不该", "怎么选", "选择困难",
        "累", "焦虑", "迷茫", "不确定", "想不清楚",
        "转行", "辞职", "side project", "副业", "搬家",
        "生活", "人生", "职业", "关系",
        "放弃", "继续", "值不值",
    ]
    bot_types = ["planner"]

    def get_context(self, **kwargs) -> str:
        return _MODELS.strip()


register(ThinkingModelsSkill())
